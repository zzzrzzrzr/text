function checkIDCard() {
    const idCardInput = document.getElementById("idCardInput").value;
    const resultDiv = document.getElementById("result");

    if (idCardInput.length !== 18) {
        resultDiv.textContent = "请输入有效的18位身份证号码！";
        resultDiv.style.color = "red";
        return;
    }

    const provinceCode = idCardInput.substring(0, 2);
    const cityCode = idCardInput.substring(2, 4);
    const fullCode = provinceCode + cityCode;

    const provinceName = getProvinceName(provinceCode);
    const cityName = idCardData[provinceCode]?.[cityCode] || "未知城市";

    if (!provinceName) {
        resultDiv.textContent = "该身份证号码不属于已知省份！";
        resultDiv.style.color = "red";
        return;
    }

    resultDiv.textContent = `归属地：${provinceName} ${cityName}`;
    resultDiv.style.color = "green";
}

